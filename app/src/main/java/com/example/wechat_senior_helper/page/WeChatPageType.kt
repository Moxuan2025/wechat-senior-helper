package com.example.wechat_senior_helper.page

/**
 * WeChat Page Type Enum
 * Represents different page types in WeChat application
 * 
 * @author moxuan
 */
enum class WeChatPageType {
    SESSION_LIST,   // Session list (main chat list)
    CHAT_PAGE,      // Chat detail page
    CONTACTS_PAGE,  // Contacts page
    DISCOVER_PAGE,  // Discover page
    ME_PAGE,        // Me/Profile page
    UNKNOWN         // Unknown page
}
